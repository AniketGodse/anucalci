first = input ("Enter first number: ")

operator = input ("Enter ooperator (+,-,*,/,%) : ")

second = input ("Enter second number : ")



if operator == "+":
    print(first + second)
    
elif operator == "-":
    print(first - second)
    
elif operator == "+":
    print(first * second)
    
elif operator == "*":
    print(first * second)
    
elif operator == "/":
    print(first / second)
    
elif operator == "%":
    print(first % second)
    
else:
    print("Invalid Operation")
    
    
    